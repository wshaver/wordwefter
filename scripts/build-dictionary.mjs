import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const defaultDictDir = path.join(projectRoot, "dicts");
const fallbackDictDir = path.join(projectRoot, "dict");
const defaultOutputFile = path.join(projectRoot, "src", "generated", "dictionary.js");

function normalizeDictionaryLine(line) {
  return String(line || "")
    .replace(/^\s*\d+\s+/, "")
    .trim()
    .toUpperCase();
}

function getDictionaryFiles(dictDir) {
  return fs.readdirSync(dictDir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && /\.txt$/i.test(entry.name))
    .map((entry) => path.join(dictDir, entry.name))
    .sort((first, second) => path.basename(first).localeCompare(path.basename(second)));
}

function formatDictionaryModule(words) {
  return [
    "// Generated by scripts/build-dictionary.mjs. Do not edit by hand.",
    `const dictionaryWords = ${JSON.stringify(words)};`,
    "const dictionaryWordSet = new Set(dictionaryWords);",
    "",
    "export { dictionaryWords, dictionaryWordSet };",
    ""
  ].join("\n");
}

function compileDictionary(options = {}) {
  const dictDir = options.dictDir || (fs.existsSync(defaultDictDir) ? defaultDictDir : fallbackDictDir);
  const outputFile = options.outputFile || defaultOutputFile;

  if (!fs.existsSync(dictDir)) {
    throw new Error(`Dictionary directory does not exist: ${dictDir}`);
  }

  const dictionaryFiles = getDictionaryFiles(dictDir);

  if (dictionaryFiles.length === 0) {
    throw new Error(`No .txt dictionary files found in: ${dictDir}`);
  }

  const seenWords = new Set();
  const words = [];

  dictionaryFiles.forEach((dictionaryFile) => {
    const sourceText = fs.readFileSync(dictionaryFile, "utf8");

    sourceText.split(/\r?\n/).forEach((line) => {
      const word = normalizeDictionaryLine(line);

      if (!/^[A-Z]+$/.test(word) || seenWords.has(word)) {
        return;
      }

      seenWords.add(word);
      words.push(word);
    });
  });

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(outputFile, formatDictionaryModule(words), "utf8");

  return {
    dictDir,
    outputFile,
    sourceFiles: dictionaryFiles.length,
    words
  };
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  const result = compileDictionary();

  console.log(`Built ${result.words.length} dictionary words from ${result.sourceFiles} .txt files.`);
  console.log(`Wrote ${path.relative(projectRoot, result.outputFile)}`);
}

export { compileDictionary, normalizeDictionaryLine };
